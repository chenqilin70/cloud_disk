package com.oracle.five.cloud_disk.beans;

import java.util.Date;
import java.util.List;

public class MyShare {
	
	 private Integer fileId;
	 private Integer shareId;
	 private Integer shareType;
	 private String fileName;
	 private Integer scanCount;
	 private Integer saveCount;
	 private Integer downloadCount;
	 private Date shareTime;
	 private Integer timeRange;
	 private List<MyShare> MySharefiles;
	
	 
	 public MyShare(String fileName, Integer scanCount, Integer saveCount,
				Integer downloadCount, Date shareTime, Integer timeRange) {
			super();
			this.fileName = fileName;
			this.scanCount = scanCount;
			this.saveCount = saveCount;
			this.downloadCount = downloadCount;
			this.shareTime = shareTime;
			this.timeRange = timeRange;
		}
	
	public MyShare(Integer fileId, Integer shareId, String fileName, Integer scanCount, Integer saveCount,
			Integer downloadCount, Date shareTime, Integer timeRange) {
		super();
		this.fileId = fileId;
		this.shareId = shareId;
		this.fileName = fileName;
		this.scanCount = scanCount;
		this.saveCount = saveCount;
		this.downloadCount = downloadCount;
		this.shareTime = shareTime;
		this.timeRange = timeRange;
	}
	public MyShare() {
		super();
	}
	public Integer getShareId() {
		return shareId;
	}
	public void setShareId(Integer shareId) {
		this.shareId = shareId;
	}
	public Integer getShareType() {
		return shareType;
	}
	
	public Integer getFileId() {
		return fileId;
	}
	public void setFileId(Integer fileId) {
		this.fileId = fileId;
	}
	
	public void setShareType(Integer shareType) {
		this.shareType = shareType;
	}
	public Integer getScanCount() {
		return scanCount;
	}
	public void setScanCount(Integer scanCount) {
		this.scanCount = scanCount;
	}
	public Integer getSaveCount() {
		return saveCount;
	}
	public void setSaveCount(Integer saveCount) {
		this.saveCount = saveCount;
	}
	public Integer getDownloadCount() {
		return downloadCount;
	}
	public void setDownloadCount(Integer downloadCount) {
		this.downloadCount = downloadCount;
	}
	public Date getShareTime() {
		return shareTime;
	}
	public void setShareTime(Date shareTime) {
		this.shareTime = shareTime;
	}
	
	public Integer getTimeRange() {
		return timeRange;
	}
	public void setTimeRange(Integer timeRange) {
		this.timeRange = timeRange;
	}
	

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public List<MyShare> getMySharefiles() {
		return MySharefiles;
	}

	public void setMySharefiles(List<MyShare> mySharefiles) {
		MySharefiles = mySharefiles;
	}
	
	 
	 
}
